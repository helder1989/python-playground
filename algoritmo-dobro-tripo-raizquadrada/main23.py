import math

# Interagindo com o usuário
numero = int(input("Registre um número: "))

# Calculando o dobro

dobro = numero * 2

# Calculando o triplo

triplo = numero * 3


# Calculando a raiz quadrada

raiz_quadrada = math.sqrt(numero)

# Exibindo os resultados

print("O dobro do número é:", dobro)
print(" O tripo do número é:", triplo)
print("A raiz quadrada do número é:", raiz_quadrada)

